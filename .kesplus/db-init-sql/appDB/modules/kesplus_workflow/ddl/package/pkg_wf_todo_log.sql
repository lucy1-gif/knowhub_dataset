	_exec_RES_REJECT INT := 0;
	_exec_RES_PASS INT := 1;
	_exec_RES_SUSPEND INT := 2;
	_PROCESS_RES_REJECT INT := 0;
	_PROCESS_RES_PASS INT := 1;
	_PROCESS_RES_SUSPEND INT := 2;
	_PROCESS_RES_CANCEL INT := 3;
	_PROCESS_RES_WITHDRAW INT := 4;
