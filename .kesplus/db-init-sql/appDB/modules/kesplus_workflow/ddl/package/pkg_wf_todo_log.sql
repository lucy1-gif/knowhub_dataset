	PROCEDURE send_pending_task(v_todo_pending_info JSON, v_variables JSON, v_role_check boolean DEFAULT FALSE);
	PROCEDURE send_pending_message(v_task_info JSON, v_variables JSON, v_role_check boolean DEFAULT FALSE);
	
	FUNCTION get_result_text(v_result varchar) RETURN varchar ;
	
