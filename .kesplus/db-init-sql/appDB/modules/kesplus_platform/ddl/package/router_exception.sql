	 * p_array
	 */
	PROCEDURE throw_exception_p(exceptionType int, code TEXT, p_array varchar[]);
	/**
     * 抛出异常,直接抛出编号和描述信息
     * jsonData		异常信息
     */
    PROCEDURE throw_exception_json(exceptionType int, jsonData jsonb);
	/**
     * 抛出异常,直接抛出编码和描述信息
     * state		异常状态编码
     */
    PROCEDURE throw_exception_state(exceptionType int, state text);
	FUNCTION get_ex() RETURN jsonb;
	FUNCTION fail(code TEXT, message TEXT DEFAULT null) RETURN JSON;
