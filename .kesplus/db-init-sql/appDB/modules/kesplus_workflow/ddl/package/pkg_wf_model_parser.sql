CREATE OR REPLACE PACKAGE "kesplus_workflow"."pkg_wf_model_parser" AUTHID CURRENT_USER AS
	/**
	* @Description 根据流程定义Key获取解析后的流程基本信息
	* @param v_process_key : 流程定义Key
	* @Return 流程基本信息
	*/
	FUNCTION parsing_bpmn_by_key(v_process_key varchar) RETURNS JSONB ;
	/**
	 * @Description 根据流程定义Id获取解析后的流程基本信息
	 * @param v_model_id : 流程定义Id
	 * @Return 流程基本信息
	 */
	FUNCTION parsing_bpmn_by_id(v_model_id varchar) RETURNS JSONB ;
	
	/**
	* @Description 解析流程信息
	* @param v_bpmn_xml : 流程定义xml
	* @Return 解析流程信息
	*/
	FUNCTION get_process_info(v_bpmn_xml XML) RETURNS JSONB;
	
	
	/**
	 * @Description 解析流程事件event
	 * @param v_bpmn_xml : 流程定义xml
	 * @param v_bpmn_type : 事件event类型
	 * @param v_process_category : 流程类型(主流程/子流程)
	 * @param v_process_id : 流程id
	 * @Return 流程事件event
	 */
	FUNCTION get_event(v_bpmn_xml XML,v_bpmn_type varchar,v_process_category varchar,v_process_id varchar) RETURNS JSONB;
	
	/**
	 * @Description 解析流程连线sequence
	 * @param v_bpmn_xml : 流程定义xml
	 * @param v_process_category : 流程类型(主流程/子流程)
	 * @param v_process_id : 流程id
	 * @Return 流程连线sequence
	 */
	FUNCTION get_sequence_flow(v_bpmn_xml XML,v_process_category varchar,v_process_id varchar) RETURNS JSONB;
	
	/**
	 * @Description 解析流程网关gateway
	 * @param v_bpmn_xml : 流程定义xml
	 * @param v_bpmn_type : 事件网关类型
	 * @param v_process_category : 流程类型(主流程/子流程)
	 * @param v_process_id : 流程id
	 * @Return 流程网关gateway
	 */
	FUNCTION get_gateway(v_bpmn_xml XML,v_bpmn_type varchar,v_process_category varchar,v_process_id varchar) RETURNS JSONB;
	
	 /**
	 * @Description 解析流程任务task
	 * @param v_bpmn_xml : 流程定义xml
	 * @param v_bpmn_type : 事件任务类型
	 * @param v_process_category : 流程类型(主流程/子流程)
	 * @param v_process_id : 流程id
	 * @Return 流程任务task
	 */
	FUNCTION get_task(v_bpmn_xml XML,v_bpmn_type varchar,v_process_category varchar,v_process_id varchar) RETURNS JSONB;
	
	/**
	 * @Description 解析流程扩展属性
	 * @param v_bpmn_xml : 流程定义xml
	 * @param v_bpmn_type : 节点类型
	 * @param v_process_category : 流程类型(主流程/子流程)
	 * @Return 流程扩展属性
	 */
	FUNCTION get_ext(v_bpmn_xml XML,v_bpmn_type varchar,v_process_category varchar) RETURNING jsonb ;
	
	/**
	 * @Description 解析流程监听器
	 * @param v_bpmn_xml : 流程定义xml
	 * @param v_bpmn_type : 节点类型
	 * @param v_process_category : 流程类型(主流程/子流程)
	 * @Return 流程监听器
	 */
	FUNCTION get_listener(v_bpmn_xml XML,v_bpmn_type varchar,v_process_category varchar) RETURNING jsonb;	
	
	/**
	 * @Description 解析流程任务类型
	 * @param v_bpmn_xml : 流程定义xml
	 * @param v_bpmn_type : 节点类型
	 * @param v_process_category : 流程类型(主流程/子流程)
	 * @Return 流程任务类型
	 */
	FUNCTION get_ext_user_task_type(v_bpmn_xml XML,v_bpmn_type varchar,v_process_category varchar) RETURNING jsonb;
	
	/**
	 * @Description 解析流程任务规则
	 * @param v_bpmn_xml : 流程定义xml
	 * @param v_bpmn_type : 节点类型
	 * @param v_process_category : 流程类型(主流程/子流程)
	 * @Return 流程任务规则
	 */
	FUNCTION get_ext_user_task_rule(v_bpmn_xml XML,v_bpmn_type varchar,v_process_category varchar) RETURNING jsonb;
	
	/**
	 * @Description 解析流程任务规则(占比)
	 * @param v_bpmn_xml : 流程定义xml
	 * @param v_bpmn_type : 节点类型
	 * @param v_process_category : 流程类型(主流程/子流程)
	 * @Return 流程任务规则
	 */
	FUNCTION get_ext_user_task_rule_proportion(v_bpmn_xml XML,v_bpmn_type varchar,v_process_category varchar) RETURNING jsonb;
	
	/**
	 * @Description 解析流程操作按钮
	 * @param v_bpmn_xml : 流程定义xml
	 * @param v_bpmn_type : 节点类型
	 * @param v_process_category : 流程类型(主流程/子流程)
	 * @Return 流程操作按钮
	 */
	FUNCTION get_ext_btn(v_bpmn_xml XML,v_bpmn_type varchar,v_process_category varchar) RETURNING jsonb;
	
	/**
	 * @Description 将节点element对应incoming连线信息补全
	 * @param v_element_list : 节点element列表
	 * @param v_sequence_flow : 连线信息
	 * @Return JSON数据
	 */
	FUNCTION add_incoming(v_element_list JSON,v_sequence_flow JSON) RETURNS jsonb;
	
	/**
	 * @Description 将节点element对应outgoing连线信息补全
	 * @param v_element_list : 节点element列表
	 * @param v_sequence_flow : 连线信息
	 * @Return JSON数据
	 */
	FUNCTION add_outgoing(v_element_list JSON,v_sequence_flow JSON) RETURNS jsonb;
	
	/**
	 * @Description 是否存在子流程嵌套子流程
	 * @param v_bpmn_xml : 流程定义xml
	 * @Return true/false
	 */
	FUNCTION exist_subprocess_nesting(v_bpmn_xml XML) RETURNS boolean ;
	
	/**
	 * 解析bpmn的XML,返回JSON数据
	 */
	FUNCTION parsing_bpmn_by_xml(bpmn_xml XML) RETURNS JSONB ;
	
	/*xml命名空间*/
	xmlns varchar[] :=  ARRAY[
						ARRAY['xsi', 'http://www.w3.org/2001/XMLSchema-instance'],
						ARRAY['bpmn', 'http://www.omg.org/spec/BPMN/20100524/MODEL'],
						ARRAY['camunda', 'http://camunda.org/schema/1.0/bpmn'],
						ARRAY['flowable', 'http://flowable.org/bpmn']
					];
END;

