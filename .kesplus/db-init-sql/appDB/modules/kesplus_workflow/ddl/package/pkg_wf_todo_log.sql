	 * 任务创建
	 * @param process 流程
	 * @param hasRetract 是否支持撤回
	 * @param resTaskInfo 返回值
	 */
	PROCEDURE task_create(v_process JSON,v_has_retract int, v_res OUT json);
	/**
	 * 任务完成
	 * @param taskId 任务id
	 * @param task 任务信息
	 * @param res 返回值
	 */
	PROCEDURE task_complete(v_task_id varchar, v_process JSON, v_res OUT json);
	/**
	 * 任务撤回
	 * @param v_task_id 任务id
	 * @param v_process 任务信息
	 * @param v_res 返回值
	 */
	PROCEDURE task_retract(v_task_id varchar, v_process JSON, v_res OUT json);
	
	/**
	 * 任务退回
	 * @param taskId 任务id
	 * @param task 任务信息
	 * @param res 返回值
	 */
	PROCEDURE task_reject(v_task_id varchar, v_process JSON, v_res OUT json);
	
	/**
	 * 任务销毁
	 * @param taskId 任务id
	 * @param variables 任务信息
	 * @param res 返回值
	 */
    PROCEDURE task_destruction(v_task_id varchar, v_variables JSON, v_res OUT json);
	
	FUNCTION save_process_draft(variables jsonb) RETURN varchar;
	
	/**
	 * 生成流程待办
